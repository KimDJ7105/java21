package exam01_PDT;

public class PDT01_int {

	public static void main(String[] args) {
		//정수형 데이터 출력
		int num;
		
		
		//2진수 출력
		num = 0b01;
		System.out.println(num);
		
		//8진수
		num = 02;
		System.out.println(02);
		
		//16진수
		num = 0xA;
		System.out.println(0xA);
		
		//큰 정수 출력해보기
		long big_num = 9876543211234566L;
		System.out.println(big_num);
	}

}
