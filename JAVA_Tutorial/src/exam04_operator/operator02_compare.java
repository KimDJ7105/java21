package exam04_operator;

public class operator02_compare {

	public static void main(String[] args) {
		int n = 10;
		int n2 = 3;
		
		//비교 연산자의 결과는 논리 연산자이다.
		
		System.out.println(n==n2);
		System.out.println(n!=n2);
		System.out.println(n>n2);
		System.out.println(n>=n2);
		System.out.println(n<n2);
		System.out.println(n<=n2);
		
	}
}
