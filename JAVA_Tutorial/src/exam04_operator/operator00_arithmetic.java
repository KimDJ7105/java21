package exam04_operator;

public class operator00_arithmetic {

	public static void main(String[] args) {
		int n = 10;
		int n2 = 3;
		
		System.out.println(n+n2);
		System.out.println(n-n2);
		System.out.println(n*n2);
		System.out.println(n/n2);
		System.out.println(n%n2);
		
		//추가 정리
		System.out.println(10/3);
		System.out.println(10/3.0);
		
		
	}
}
