package exam01_PDT;

public class PDT02_float {

	public static void main(String[] args) {
		//float
		System.out.println(3.14f);
		System.out.println(3.14F);
		
		//double
		System.out.println(3.14D);
		System.out.println(3.14d);
		System.out.println(3.14);
		
		//size
		//float f = 3.14; 오류 발생.
		float f = 3.14f;
	}

}
